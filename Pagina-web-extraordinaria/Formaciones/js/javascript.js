//Logica servicios
let servicios = document.getElementById('services');
let menuservicios = document.getElementById('menu-servicios');

servicios.addEventListener('mouseover', function () {
    menuservicios.style.display = 'block';
});

menuservicios.addEventListener('mouseover', function () {
    menuservicios.style.display = 'block';
});

servicios.addEventListener('mouseout', function () {
    menuservicios.style.display = 'none';
});

menuservicios.addEventListener('mouseout', function () {
    menuservicios.style.display = 'none';
});

